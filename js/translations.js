/*
 * HOW TO USE THE TRANSLATION SYSTEM
 *
 * 1. STATIC TEXT: Use data-lang-en and data-lang-es attributes
 *
 *    <p data-lang-en>This is in English</p>
 *    <p data-lang-es>Esto está en Español</p>
 *
 * 2. DYNAMIC TEXT: Use data-translate attribute
 *
 *    <button data-translate="common.submit">Submit</button>
 *
 *    This will automatically update the button text when language is changed
 *
 * 3. FORM PLACEHOLDERS: Use data-translate-placeholder attribute
 *
 *    <input type="text" data-translate-placeholder="contact.name" placeholder="Name">
 *
 * 4. JAVASCRIPT: Get translations programmatically
 *
 *    // Example: Create a notification
 *    const message = getTranslation('validation.success');
 *    showNotification(message);
 *
 *    // Example: Create new elements
 *    const btn = document.createElement('button');
 *    btn.textContent = getTranslation('common.apply');
 *    document.body.appendChild(btn);
 *
 * 5. UPDATING CONTENT: If you add new content dynamically
 *
 *    // After adding new elements with data-translate attributes
 *    translateDynamicContent();
 */
